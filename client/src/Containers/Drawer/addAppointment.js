import { connect } from "react-redux";
import { withRouter } from "react-router-dom";
import AddAppointmentDrawer from "../../Components/Drawer/addAppointment";
import { close } from "../../modules/drawer";
import { DRAWER } from "../../constant";
import { getMedications } from "../../modules/medications";
import { getAppointments, addAppointment, addCarePlanAppointment } from "../../modules/appointments";
import {
    markFavourite , 
    getFavourites , 
    removeFavourite} from "../../modules/favouritesData/index";

    
const mapStateToProps = state => {
    const {
        drawer: { visible, loading, data: { type, payload = {} } = {} },
        patients, treatments, care_plans, static_templates, providers,
        favourites_data={},
        pages:{favourite_medical_test_ids = []} ={}
    } = state
    return {
        visible: visible && type === DRAWER.ADD_APPOINTMENT,
        loading,
        payload,
        treatments,
        patients,
        care_plans,
        static_templates,
        providers,
        favourites_data,
        favourite_medical_test_ids
    };
};

const mapDispatchToProps = dispatch => {
    return {
        close: () => dispatch(close()),
        addAppointment: data => dispatch(addAppointment(data)),
        addCarePlanAppointment: (data, carePlanId) => dispatch(addCarePlanAppointment(data, carePlanId)),
        getMedications: (id) => dispatch(getMedications(id)),
        getAppointments: (id) => dispatch(getAppointments(id)),
        markFavourite : (payload) => dispatch(markFavourite(payload)),
        getFavourites : ({type}) => dispatch(getFavourites({type})),
        removeFavourite: ({typeId,type}) => dispatch(removeFavourite({typeId,type}))
    };
};

export default withRouter(connect(
    mapStateToProps,
    mapDispatchToProps
)(AddAppointmentDrawer));